public class MathExamples
{
    public static void main(String[] args)
    {
        int n = 0;
        double d = 0.0;
        
        // Math methods
        n = Math.abs(-17);
        System.out.println(n);
        
        d = Math.abs(-39.65);
        System.out.println(d);
        
        d = Math.pow(10.0, 3.0);
        System.out.println(d);
        
        n = Math.max(25, 50);
        System.out.println(n);
        
        n = Math.min(25, 50);
        System.out.println(n);
        
        d = Math.sqrt(81.0);
        System.out.println(d);
    }
}