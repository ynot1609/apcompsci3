public class Student
{
    // instance variables
    private int age;
    
    // constructors
    public Student()
    {
        age = 16;
    }
    
    // accessor methods
    public int getAge()
    {
        return age;
    }
    
    // mutator methods
    public void setAge(int newAge)
    {
        age = newAge;
    }
}