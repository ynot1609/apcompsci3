public class WhilePractice
{
    public static void main(String[] args)
    {
        int count = 1;
        while (count <= 25)
        {
            System.out.print(count + " ");
            count++;
        }
        
        System.out.println();
        
        int i = 50;
        while (i <= 70)
        {
            System.out.print(i + " ");
            i++;
        }
    }
}
