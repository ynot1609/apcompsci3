public class ForPractice
{
    public static void main(String[] args)
    {
        for (int i = 0; i <= 25; i++)
        {
            System.out.print(i + " ");
        }
        
        System.out.println();
        
        for (int i = 10; i >= 1; i--)
        {
            System.out.print(i + " ");
        }
        
        System.out.println();
        
        for(int i = 1; i < 40; i += 2)
        {
            System.out.print(i + " ");
        }
    }
}