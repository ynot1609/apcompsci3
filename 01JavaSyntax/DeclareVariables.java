public class DeclareVariables
{
    public static void main(String[] args)
    {
        // Declare variables
        int distance;
        double radius;
        
        // Initialize variables
        distance = 875;
        radius = 28.975;
        
        // Display values to the terminal
        System.out.println(distance);
        System.out.println(radius);
        
        // Update variables
        distance = 1025;
        radius = 32.625;
        
        // Display values to the terminal
        System.out.println(distance);
        System.out.println(radius);
    }
}