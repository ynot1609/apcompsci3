public class Variables
{
    public static void main(String[] args)
    {
        // Primitive data types
        int age = 17;
        System.out.println("My age: " + age);
    
        double gradePointAverage = 3.7821;
        System.out.println("My GPA: " + gradePointAverage);
        
        boolean bndsStudent = true;
        System.out.println("BNDS Student? " + bndsStudent);
        
        // String object data type
        String name = "Nicholas Cage";
        System.out.println("My name is: " + name);
        
        String courseName = "Computer Science";
        System.out.println("My class is: " + courseName);
        
        int gradeLevel = 12;
        System.out.println("Grade level is: " + gradeLevel);
    }
}



